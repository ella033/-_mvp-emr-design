export type NHICErrorCode = keyof typeof NHIC_ERROR_CODES;

/**
 * 에러 코드에 해당하는 메시지를 반환합니다.
 * @param errorCode 에러 코드
 * @returns 에러 메시지 또는 기본 메시지
 */
export function getErrorMessage(errorCode: string): string {
  return NHIC_ERROR_CODES[errorCode as NHICErrorCode] || `알 수 없는 오류가 발생했습니다 (${errorCode})`;
}

/**
 * EWS 또는 EWG 또는 IWG로 시작하는 에러 코드인지 확인합니다.
 * @param errorCode 에러 코드
 * @returns EWS/EWG/IWG 에러 여부 - IWG는 시스템상 발생 코드로 에러인지 정상인지 판단이 어려움
 */
export function isNHICError(errorCode: string): boolean {
  return errorCode.startsWith('EWS') || errorCode.startsWith('EWG') || errorCode.startsWith('IWG');
}